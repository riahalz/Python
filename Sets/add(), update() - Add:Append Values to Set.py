### Sets - Add/Append Values to a Set ###


Set1 = {"carrot", "potato", "lettuce", "beans", "corn"}

# Add/append a value to the set using add() function

Set1.add("capsicum")

print (Set1)


Set2 = {"a", "b", "c", "d", "e"}

# Add/append values to the set using update() function

Set2.update(["cauliflower", "radish"])

print (Set2)
