### Tuples - Sort Values in Tuple ###


## Sort the tuple - least to greatest values
tuple = (10, 2, 3, 34, 78, 190, 500)

# Sort function
sorted(tuple)

print (sorted(tuple))


## Sort the tuple - order of alphabetical characters
tuple2 = ('p', 'r', 'o', 'g', 'r', 'a', 'm')

# Sort function
sorted(tuple2)

print (sorted(tuple2))
