### Tuples ###

# Tuples are used to store multiple items in a single variable.
# Ex: tuple = (2, "element")
# Tuples are ordered and unchangeable, unlike lists which are changeable.
# Tuples are written with round brackets ()
# A tuple can have elements of different types.
# Tuple packing: creating a tuple without parentheses (), ex: 1, 2, 3


## Print a tuple
tuple = (10)
print(tuple)


## Print a set of elements as a tuple
tuple2 = 1, 2, "element"
print (tuple2)


## Print data type of a tuple
tuple3 = ("hello")
print (type(tuple3))


## Print element in tuple with specific index
tuple4 = (1, 2, 3, 4, 5, "AA", 6)

# print element with specific index
tuple4[4]
print (tuple4[4])

tuple4[-3]
print (tuple4[-3])

# print elements in range
tuple4[2:4]
print (tuple4[2:4])


