### Index of value of Nested Tuples/Lists inside a Tuple ###


# This is a tuple having a list and another tuple inside it.
tuple5 = (1, 2, "a", (3, 4), [10, 20])


# Index of value inside list in the tuple
tuple5[4][0]
print(tuple5[4][0])


# Index of value inside tuple in the tuple
tuple5[3][1]
print(tuple5[3][1])
