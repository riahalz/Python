### upper(), lower(), casefold() ###


## Convert lowercase text to upper case
text = "text"
# upper()
text.upper()
print (text.upper()) # Output: TEXT

## Convert lowercase text to upper case
text = "TEXT"
# lower()
text.lower()
print (text.lower()) # Output: text


## Print all text in lowercase
text2 = "hsdiIHIOHhi"
# casefold()
text2.casefold()
print(text2.casefold())
