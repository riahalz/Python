### isprintable() - Check if string is printable or not


"Hello World".isprintable()
print("Hello World".isprintable())
# Output: True

"Hello \\n World".isprintable()
print("Hello \\n World".isprintable())
# Output: True

"Hello \n World".isprintable()
print("Hello \n World".isprintable())
# Output: False

" ".isprintable()
print(" ".isprintable())
# Output: True
