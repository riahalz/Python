### format() - Insert String in Specific Place in Main String


name = "Riya"
age =  30
ethnicity = "Indian"

# format(str1, str2)
"My name is {} and I am {} years old.".format(name,age)

print ("My name is {} and I am {} years old. I am {}.".format(name,age,ethnicity))
# Output: My name is Riya and I am 30 years old. I am Indian.

