### Split() - Separation of String Elements ###

str = "This is a string"

# Split strings using split() operator
str.split(" ")

print(str.split(" "))
# Output: ['This', 'is', 'a', 'string']

str1 = "one, two, three"

# Split strings using split() operator
str1.split(" ")

print(str1.split(" "))
# Output: ['one,', 'two,', 'three']


