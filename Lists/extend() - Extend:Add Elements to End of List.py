### Lists - Extend/Add elements of a list to end of current list ###


List1 = ["carrot", "potato", "lettuce", "beans", "corn"]
List2 = ["a","b","c","d","e"]

# Add elements from List2 to end of List1

List1.extend(List2)

print(List1)
# Output: ['carrot', 'potato', 'lettuce', 'beans', 'corn', 'a', 'b', 'c', 'd', 'e']
