### Lists - Multiplying Values in a List ###

a = [10, 20, 30, 40, 50]
b = [60, 70, 80, 90, 100]

print (a*3)
# Output: [10, 20, 30, 40, 50, 10, 20, 30, 40, 50, 10, 20, 30, 40, 50]

print (b*5)
# Output: [60, 70, 80, 90, 100, 60, 70, 80, 90, 100, 60, 70, 80, 90, 100, 60, 70, 80, 90, 100, 60, 70, 80, 90, 100]
