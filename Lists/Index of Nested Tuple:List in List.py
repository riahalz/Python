### Index of value of Tuples/Lists inside a List ###

list = [10,23,"34A",(2,3),[5,8],2]

# Index of value inside tuple in the list
# list[index of tuple elements][index of value inside tuple element]
list[4][0]
print(list[4][0])

# Index of value inside list in the list
# list[index of list element][index of value inside list element]
list[3][1]
print(list[3][1])
